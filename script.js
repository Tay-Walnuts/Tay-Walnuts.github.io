


function Heading1(elementID){
    const element = document.getElementById(elementID)

    if (element && element.id === "H1") {
        if (element.style.color === "aliceblue"){
        element.style.color = "lightblue";
        }
        else if (element.style.color === "lightblue"){
        element.style.color = "cornflowerblue";
        }

        else {
        element.style.color = "aliceblue";
        }
    }
}



